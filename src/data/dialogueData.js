// ─── ENDING DETERMINATION ───

/**
 * Determine ending based on Inner/Outer scores
 * Priority: Dreamer > Wanderer > Reborn > Keeper > Searcher > Present
 */
export function determineEnding(innerScore, outerScore) {
  // Priority order
  const priorityOrder = [0, 2, 5, 1, 4, 3]; // indices: Dreamer, Wanderer, Reborn, Keeper, Searcher, Present

  for (const idx of priorityOrder) {
    if (endings[idx].scoreCondition(innerScore, outerScore)) {
      return endings[idx];
    }
  }

  // Fallback to Present
  return endings[3];
}

